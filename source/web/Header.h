#ifndef _HEADER_H_
#define _HEADER_H_

#include <string>

namespace Http
{
	namespace Server
	{
		class Header
		{

		private:

			std::string m_szName;
			std::string m_szValue;

		public:

			//////////////////////////////////////////////////////////////////////////////
			//Constructor
			///////////////////////////////////////////////////////////////////////////////
			Header();

			///////////////////////////////////////////////////////////////////////////////
			//Copy Constructor
			///////////////////////////////////////////////////////////////////////////////
			Header(const Header& header);

			///////////////////////////////////////////////////////////////////////////////
			//Assignment Operator
			///////////////////////////////////////////////////////////////////////////////
			Header& operator=(const Header& header);

			///////////////////////////////////////////////////////////////////////////////
			//Destructor
			///////////////////////////////////////////////////////////////////////////////
			~Header();

			///////////////////////////////////////////////////////////////////////////////
			//Accessors
			///////////////////////////////////////////////////////////////////////////////
			const std::string& GetName()	const { return this->m_szName;	}
			const std::string& GetValue()	const { return this->m_szValue; }

			///////////////////////////////////////////////////////////////////////////////
			//Mutators
			///////////////////////////////////////////////////////////////////////////////
			void SetName(const std::string& szName)		{ this->m_szName = szName;		}
			void SetValue(const std::string& szValue)	{ this->m_szValue = szValue;	}
		};
	}
}

#endif //_HEADER_H_
