#ifndef _IOBSERVER_H_
#define _IOBSERVER_H_

class IObserver
{

private:

	///////////////////////////////////////////////////////////////////////////////
	//Copy Constructor
	///////////////////////////////////////////////////////////////////////////////
	IObserver(const IObserver& iObserver) {}

	///////////////////////////////////////////////////////////////////////////////
	//Assignment Operator
	///////////////////////////////////////////////////////////////////////////////
	IObserver& operator=(const IObserver& iObserver) { return *this; }


public:

	//////////////////////////////////////////////////////////////////////////////
	//Constructor
	///////////////////////////////////////////////////////////////////////////////
	IObserver();

	///////////////////////////////////////////////////////////////////////////////
	//Destructor
	///////////////////////////////////////////////////////////////////////////////
	virtual ~IObserver() = 0;

	virtual void onNotify();

};


#endif //_IOBSERVER_H_