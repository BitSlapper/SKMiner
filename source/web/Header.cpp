#include "Header.h"


namespace Http
{
	namespace Server
	{
		//////////////////////////////////////////////////////////////////////////////
		//Constructor
		///////////////////////////////////////////////////////////////////////////////
		Header::Header()
		{

		}

		///////////////////////////////////////////////////////////////////////////////
		//Copy Constructor
		///////////////////////////////////////////////////////////////////////////////
		Header::Header(const Header& header)
		{
			this->m_szName = header.GetName();
			this->m_szValue = header.GetValue();
		}

		///////////////////////////////////////////////////////////////////////////////
		//Assignment Operator
		///////////////////////////////////////////////////////////////////////////////
		Header& Header::operator = (const Header& header)
		{
			this->m_szName = header.GetName();
			this->m_szValue = header.GetValue();

			return *this;
		}

		///////////////////////////////////////////////////////////////////////////////
		//Destructor
		///////////////////////////////////////////////////////////////////////////////
		Header::~Header()
		{

		}

	}
}